def floyd_warshall(matriz):
    n = len(matriz)
    dist = [linha[:] for linha in matriz]

    for k in range(n):
        for i in range(n):
            for j in range(n):
                if dist[i][j] > dist[i][k] + dist[k][j]:
                    dist[i][j] = dist[i][k] + dist[k][j]

    return dist